console.log(5, typeof 5);           // Returns number
console.log(5.2, typeof 5.2);       // Returns number
console.log("text", typeof "text"); // Returns string
console.log(true, typeof true);     // Returns boolean
console.log(false, typeof false);   // Returns boolean
console.log(null, typeof null);      // Returns object
console.log(undefined, typeof undefined); // Returns undefined
console.log(2n, typeof 2n);          // Returns bigint